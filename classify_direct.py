def main():
    with open(INPUT_FILE, encoding="utf-8") as f:
        rows = list(csv.DictReader(f))

    out_rows = []
    for i, r in enumerate(rows, start=1):
        if i in MANUAL:
            blog, cat, solve, angle = MANUAL[i]
        else:
            blog, cat, solve, angle = False, "기타", False, ""

        out_rows.append({
            **r,
            "is_blog_material": str(blog),
            "category": cat,
            "can_i_solve": str(solve),
            "blog_angle": angle,
            "desc_summary": SUMMARIES.get(i, ""),
        })

    blog_count = sum(1 for r in out_rows if r["is_blog_material"] == "True")
    solve_count = sum(1 for r in out_rows if r["can_i_solve"] == "True")

    fieldnames = list(rows[0].keys()) + ["is_blog_material", "category", "can_i_solve", "blog_angle", "desc_summary"]
    with open(OUTPUT_FILE, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(out_rows)

    print(f"총 {len(out_rows)}개")
    print(f"블로그 소재: {blog_count}개")
    print(f"내가 해결 가능: {solve_count}개")
    print(f"저장: {OUTPUT_FILE}")

if __name__ == "__main__":
    main()
